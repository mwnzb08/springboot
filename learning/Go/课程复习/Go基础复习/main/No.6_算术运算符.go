package main

import "fmt"

func main() {
	//数组在相同维度的情况下可以进行比较,完全一致则为true
	a := [...] int{1, 2, 3}
	b := [...] int{1, 3, 2}
	c := [...] int{1, 2, 3}
	fmt.Println(a == b, a == c)

	//&^ 按位零适用于权限控制，例如:
	//如果父位是0则为0，父位不为0，子位为1 则为0
	d := 7     //0111
	fmt.Print(d&1 == 1) //011(1) 000(1)
	d = d &^ 1 //0110
	fmt.Print(d&1 == 1)// 011(0) 000(1)
	d = d &^ 8//10000
	fmt.Print(d&8 == 8)// (0)110 (1)000

}
